// ArithmeticOperators
console.log('Arithmetic Operators:')
// Addition
var x = 10, y=4;
var sum = x + y
console.log(`Sum: ${x} + ${y} = ${sum}`)

// Subtraction
x = 10, y=4;
var diff = x + y
console.log(`Diff: ${x} + ${y} = ${diff}`)

// Multiplication
x = 10, y=4;
var prod = x - y
console.log(`Prod: ${x} - ${y} = ${prod}`)

// Division
x = 10, y=4;
var div = x / y
console.log(`Div: ${x} / ${y} = ${div}`)

// Modulus
x = 10, y=4;
var mod = x % y
console.log(`Mod: ${x} % ${y} = ${mod}`)

// Exponentiation
x = 10, y=4;
var exp = x ** y
console.log(`Exp: ${x} ** ${y} = ${exp}`)


// Comparison Operators
console.log('\n\nComparison Operators:')
// Equal to
var a = 10, b = 5, str = '10';
console.log(`${a} == ${b} = ${a == b}`)
console.log(`${a} != ${b} = ${a != b}`)
console.log(`${a} == str(${str}) = ${a == str}`)
console.log(`${a} === ${b} = ${a === b}`)
console.log(`${a} !== ${b} = ${a !== b}`)
console.log(`${a} === str(${str}) = ${a === str}`)
console.log(`${a} > ${b} = ${a > b}`)
console.log(`${a} >= ${b} = ${a >= b}`)
console.log(`${a} < ${b} = ${a < b}`)
console.log(`${a} <= ${b} = ${a <= b}`)